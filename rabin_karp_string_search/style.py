"""Єдина візуальна тема для всіх візуалізацій: палітра кольорів і параметри matplotlib.

Усі кольори зведено в одне місце іменованими константами, щоб змінити вигляд
рисунків можна було з однієї точки. Колірна мова послідовна в усьому розборі й
продовжує мову попередніх рядкових алгоритмів серії
([наївного](https://github.com/MarynaShavlak/algo-naive-string-search),
[KMP](https://github.com/MarynaShavlak/algo-knuth-morris-pratt-search),
[Боєра-Мура](https://github.com/MarynaShavlak/algo-boyer-moore-string-search)),
але **розвиває її під ХЕШІ**, бо Рабін-Карп порівнює не символи, а числа.

**Хеш як «бейдж-лічильник»:**

* 🔢 хеш вікна й хеш шаблону малюються підписаними **бейджами-числами** — їх
  порівнюють замість символів;
* 🟢 **зелений** — справжній збіг (хеш збігся **і** посимвольна перевірка пройшла);
* 🟠 **помаранчевий** — збіг **ХЕШУ** (числа рівні — кандидат на перевірку);
* 🔴 **червоний** — **КОЛІЗІЯ**: хеші рівні, а рядки різні (перевірка відкидає);
* ⬜ **нейтральний** — символ/вікно, ще не порівняне; 🩶 **затемнений** — позаду вікна.

**Rolling hash (ковзне оновлення за O(1)):**

* 🔻 символ, що **виходить** (лівий) — його внесок віднімають;
* 🔺 символ, що **входить** (правий) — його додають;
* окрема барва «бейджа» хешу, що **котиться** вздовж тексту.

**Криві складності:**

* окремі барви для Рабіна-Карпа :math:`O(n+m)` (середній) та :math:`O(n\\cdot m)`
  (найгірший — багато колізій), а також для **чотирьох-алгоритмного** порівняння
  (наївний / KMP / Боєра-Мура / Рабіна-Карпа).

Символи завжди малюють **моноширинним** шрифтом у підписаних комірках, тож текст,
шаблон і числа вирівнюються рівними стовпчиками.
"""

